export async function insertData(token){
  const api = "http://localhost:8080/api/v1/admin/hotels"
  const method = "POST"
    for(const hotel of DUMMY_HOTEL_DATA){
        await fetch(api, {
            method,
            headers: {
                "Content-Type": "application/json;charset=utf-8",
                Authorization: `Bearer ${token}`,
            },
            body: JSON.stringify(hotel),
        });
    }
}

export async function activeHotel(token){
    const method = "PATCH";
    let index = 1
    for(const hotel of DUMMY_HOTEL_DATA){
        const api = `http://localhost:8080/api/v1/admin/hotels/${index}/activate`
        await fetch(api, {
            method,
            headers : {
                'Content-Type': 'application/json;charset=utf-8',
                'Authorization': `Bearer ${token}`
            },
        })
        index++;
    }
}

export async function insertRooms(token){
  const method = "POST"
  for(const hotel of DUMMY_ROOM_DATA){
    let api = `http://localhost:8080/api/v1/admin/hotels/${hotel.hotelId}/rooms`;
    for(const room of hotel.rooms){
      await fetch(api, {
        method,
        headers : {
          'Content-Type': 'application/json;charset=utf-8',
          'Authorization': `Bearer ${token}`
        },
        body : JSON.stringify(room)
      });
    }
  }
}